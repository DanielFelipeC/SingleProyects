    public boolean validarCredencialesCorreo(String username, String password) {
        boolean result = false;
        try {
            Properties props;

            props = System.getProperties();
            if (username.contains("gmail")) {
                props.put("mail.smtp.host", "smtp.gmail.com");
                props.put("mail.smtp.socketFactory.port", "465");
                props.put("mail.smtp.socketFactory.class", "javax.net.ssl.SSLSocketFactory");
                props.put("mail.smtp.auth", "true");
                props.put("mail.smtp.port", "465");
            } else if (username.contains("yahoo")) {
                props.put("mail.smtp.auth", "true");
                props.put("mail.smtp.starttls.enable", "true");
                props.put("mail.smtp.host", "smtp.yahoo.com");
                props.put("mail.smtp.port", "465");
            } else if (username.contains("hotmail") || username.contains("outlook")) {
                props.put("mail.smtp.host", "smtp.live.com");
                props.put("mail.smtp.auth", "true");
                props.put("mail.smtp.port", "25");
                props.put("mail.smtp.starttls.enable", "true");
            } else {
                props.put("mail.smtp.host", "smtp.gmail.com");
                props.put("mail.smtp.socketFactory.port", "465");
                props.put("mail.smtp.socketFactory.class", "javax.net.ssl.SSLSocketFactory");
                props.put("mail.smtp.auth", "true");
                props.put("mail.smtp.port", "465");
            }

            Session session = Session.getInstance(props, null);
            Transport transport = session.getTransport("smtp");
            transport.connect(username, password);
            transport.close();
            result = true;

        } catch (AuthenticationFailedException e) {
            System.out.println(e.toString() + "SMTP: Autenticacion Fallida");
        } catch (MessagingException e) {
            System.out.println(e.toString() + "SMTP: Messaging Exception Occurred");
        } catch (Exception e) {
            System.out.println(e.toString() + "SMTP: Exception Desconocida");
        }
        return result;
    }

    public void iniciarSesion() {
        correoEmail = correo.getText();

        char[] charsPassword = contra.getPassword();

        passwordEmail = "";

        for (int i = 0; i < charsPassword.length; i++) {
            passwordEmail += charsPassword[i];
        }

//        passwordEmail = contra.getText();
        if (correoEmail.equals(new String())) {

            JOptionPane.showMessageDialog(null, "El Correo no puede estar vacio");

        } else if (passwordEmail.equals(new String())) {

            JOptionPane.showMessageDialog(null, "La Contrase√±a no puede estar vacia");

        } else if (validarCredencialesCorreo(correoEmail, passwordEmail)) {
            windowMsg = new FormMsg();

            FormMsg correazo = new FormMsg();
            correazo.setMiEmail(new Correo(correoEmail, passwordEmail));

            Dimension pantalla = Toolkit.getDefaultToolkit().getScreenSize();
            Dimension ventana = windowMsg.getSize();
            
            Point pointo = new Point((pantalla.width - ventana.width) / 2, (pantalla.height - ventana.height) / 2);
            
            windowMsg.setLocation(pointo);

            this.dispose();

            URL url = ClassLoader.getSystemResource("mail/social.png");
            ImageIcon icon = new ImageIcon(url);
            windowMsg.setIconImage(icon.getImage());

            windowMsg.setTitle("Enviar Correo Electronico");
            windowMsg.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(null, "El correo o la contrase√±a son incorrectas, intentelo nuevamente");
        }

    }
